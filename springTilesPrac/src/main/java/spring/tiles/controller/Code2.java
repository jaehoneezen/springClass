package spring.tiles.controller;

public class Code2 {
	private String code;
	private String label;
	
	public Code2() {
	}
	
	public Code2(String code, String value) {
		this.code = code;
		this.label = value;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String value) {
		this.label = value;
	}


}
